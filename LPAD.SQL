## LPAD() FUNCTION
### The Oracle LPAD() function is used to padding the left side of a string with a specific set of characters. The function is useful for formatting the output of a query.


FROM DUAL;
--SYNTAX
--SELECT LPAD(EXPRESSION1,N,EXPRESSION2);
--SELECT LPAD('HELLO',7,'*')FROM DUAL;--IT WILL ADD A * BEFORE STRING TO COMPLETE GIVEN NUMBER AS 6
--IT WILL ADD A * BEFORE STRING TO COMPLETE GIVEN NUMBER AS 6
SELECT LPAD('HELLO',7,'*')FROM DUAL; --ADD 1 * TO LEFT SIDE 
SELECT LPAD('HELLO',10,'*')FROM DUAL; --ADD 5 * TO LEFT SIDE 
SELECT LPAD('HELLO',8,'_')FROM DUAL; --ADD 5 - TO LEFT SIDE 
SELECT LPAD('OracleSSSS',10,'+') FROM DUAL;--ADD 0 + TO LEFT SIDE
SELECT LPAD('DATABASES',10,'+') FROM DUAL;--ADD 1+ TO LEFT SIDE
